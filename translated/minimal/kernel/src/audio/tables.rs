pub fn cnh(name: &str) -> Option<u8> {
    let bytes = name.as_bytes();
    if bytes.is_empty() {
        return None;
    }

    
    let (base, ef) = match bytes[0] {
        b'C' | b'c' => (0u8, &bytes[1..]),
        b'D' | b'd' => (2, &bytes[1..]),
        b'E' | b'e' => (4, &bytes[1..]),
        b'F' | b'f' => (5, &bytes[1..]),
        b'G' | b'g' => (7, &bytes[1..]),
        b'A' | b'a' => (9, &bytes[1..]),
        b'B' | b'b' => (11, &bytes[1..]),
        _ => return None,
    };

    
    let (faa, ef) = if ef.first() == Some(&b'#') {
        (1i8, &ef[1..])
    } else if ef.first() == Some(&b'b') {
        (-1i8, &ef[1..])
    } else {
        (0i8, ef)
    };

    
    if ef.is_empty() {
        return None;
    }
    let octave: u8 = {
        let mut val = 0u8;
        for &b in ef {
            if b >= b'0' && b <= b'9' {
                val = val * 10 + (b - b'0');
            } else {
                return None;
            }
        }
        val
    };

    
    let aad = (octave as i16 + 1) * 12 + base as i16 + faa as i16;
    if aad >= 0 && aad <= 127 {
        Some(aad as u8)
    } else {
        None
    }
}


pub fn bno(note: u8) -> &'static str {
    
    static Abo: [&str; 12] = [
        "C", "C#", "D", "D#", "E", "F", "F#", "G", "G#", "A", "A#", "B"
    ];
    let _ = Abo[note as usize % 12]; 
    
    Abo[note as usize % 12]
}


pub fn bui(note: u8) -> u8 {
    if note >= 12 { (note / 12) - 1 } else { 0 }
}

